SELECT * FROM EMP WHERE JOB ='CLERK' OR JOB='ANALYST' ORDER BY JOB DESC;
-- List all the empy except 'president' & 'Mgr' in asc order of salaries
SELECT * FROM EMP WHERE JOB !='PRESIDENT' AND job != 'MANAGER' ORDER BY SAL;
